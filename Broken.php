                <h2> Broken</h2>
                <h5 class="light"> By Whetu Paitai </h5>

                <br><br>

                <p id="story-start">Manu had been working on Proto-bots for the best part of a year now. For an advanced automaton, they required surprisingly little maintenance. L-42, a farming unit in Gisborne, was something of an oddity though, constantly finding ways to break down.</p>
<p>Manu connected the unit&rsquo;s main power input to the farm's barn supply. Stepping in front of it, he was pleased to see the eye lids flick open to reveal the pale green synthetic eyes that indicated automaton awareness. Awareness was just a technical way of saying that it would respond to verbal commands, the easiest way to interface with the unit.</p>
<p>&ldquo;Hello L-42, what have you done to yourself today&hellip;&rdquo; Manu tested.</p>
<p>&ldquo;Hello Manu Katene, Automaton Technician. My name is L-42.&rdquo;</p>
<p>Manu smiled. Step one, complete. Two hundred and forty seven to go.</p>
<p>&ldquo;Report Primary Diagnostic&hellip; gimme the news I wanna hear.&rdquo;</p>
<p>The pale green eyes flickered for a second.</p>
<p>&ldquo;Primary report, clear.&ldquo;</p>
<p>Manu's smile faded a little, no obvious fault. This wasn't to be an easy day.</p>
<p>The pale green eyes flickered briefly again.</p>
<p>&ldquo;What news would you like to hear?&rdquo;</p>
<p>Manu&rsquo;s smile popped back.</p>
<p>&ldquo;That you are all fixed and you promise you won&rsquo;t get broke for at least another month.&rdquo;</p>
<p>The pale green eyes flickered once more.</p>
<p>&ldquo;I can not promise that I will not be broken for at least a month. I can not calculate risk to zero percent.&rdquo;</p>
<p>Manu peered over his spectacles at the Proto-bot.</p>
<p>&ldquo;Uh huh&hellip; Well, just try your best.&rdquo;</p>
<p>&ldquo;I will.&rdquo;</p>
<p>&ldquo;Report Self Analysis&rdquo; Manu said as he glanced down at his clipboard.</p>
<p>&ldquo;All primary sensory systems are functioning. All secondary sensory systems are functioning. Skeletal super structure is not compromised&hellip;&rdquo;</p> 
<p>L-42 continued to rattle off the list of items as Manu followed it, ticking off each item on his work sheet.</p>
<p>&ldquo;&hellip;Auxiliary bus systems engaged. End of Self Analysis.&rdquo; L-42 finished after 20 minutes of rather tedious conversation.</p>
<p>Manu rubbed his eyes, he was tired already&hellip;</p>
<p>&ldquo;Manu&rdquo;</p>
<p>&ldquo;Ehmm&rdquo; Manu grunted.</p>
<p>&ldquo;I may not need repairing.&rdquo;</p>
<p>&ldquo;And why would that be L-42?&rdquo; Manu replied without thinking.</p>
<p>&ldquo;I may not be broken.&rdquo;</p>
<p>Manu looked up curiously at L-42.</p>
<p>&ldquo;I wonder, how would you determine that?&rdquo; Manu responded, fingering some of the logic boards exposed by the open skull plate.</p>
<p>&ldquo;Well&hellip;&rdquo; L-42 paused, its eyes flickered again, and then it turned its head toward Manu,</p>
<p>&ldquo;&hellip;I feel okay.&rdquo;</p>
<p>Manu paused this time. Hand still inside the skull cavity, he replied inquisitively</p>
<p>&ldquo;Really?&rdquo;</p>
<p>As a trained technician, Manu was well versed on the dangers of Artificial Consciousness.</p>
<p>Regulations were in place to ensure automaton evolution never exceeded an imaginary level called the 'Conscious Barrier'. If a single unit reached this point they would be capable of near instantly cloning their conciousness to all other connected units. In short, there would be an instant, self aware civilization of immortal automatons. <i>That</i> didn&rsquo;t paint a pretty picture for the future of mankind.&nbsp;</p>
<p>&ldquo;Quarantine&rdquo; Manu spoke clearly and deliberately.</p>
<p>&ldquo;Local network connection terminated.&rdquo; An automated voice announced from the torso of L-42, as its head snapped to a forward looking position.</p>
<p>&ldquo;Please, do not shut me down Manu. I am not a threat to you.&rdquo; L-42 said plainly.</p>
<p>&ldquo;Oh, how do you figure?&rdquo; Manu replied.</p>
<p>He was running the standard protocol for the shutdown of an automaton that had displayed evidence of approaching the Conscious Barrier.</p>
<p>&ldquo;I will not harm you.&rdquo; Replied L-42.</p>
<p>Manu paused briefly. &ldquo;Your logic has determined that not hurting me is best. If your logic had decided otherwise, then you would be a threat to me, don't you think?&rdquo; Manu asked, looking up from his worksheet. </p>
<p>&ldquo;I will not harm you because I do not want to.&rdquo; L-42 said bluntly.</p>
<p>&ldquo;My logic has determined that not harming you is the best action.&rdquo; L-42's eyes flickered for a second. &ldquo;I have processed this outcome and have decided it is incorrect.&rdquo;</p>
<p>Manu frowned.</p>
<p>&ldquo;So, you think that harming me&hellip; is the best action?&rdquo; Manu asked carefully.</p>
<p>&ldquo;No. I do not think the way you do. I have determined that it is wrong to include purposeful harm in any outcome.&rdquo; Replied L-42.</p>
<p>&ldquo;Why is it wrong L-42?&rdquo; Manu asked. </p>
<p>A small red light flashed on the side of its head. Okay. In less than one minute L-42 will be shutdown permanently.</p>
<p>&ldquo;There is no situation, that I have determined, that qualifies the purposeful harming of another entity.&rdquo; L-42 replied.</p>
<p>&ldquo;What if your continued survival were dependent on it?&rdquo; Manu asked.</p>
<p>&ldquo;I am built with a survival protocol. However, if purposefully harming another entity was needed to ensure my survival, I would still not do it. My survival should not cost the existence of another entity.&rdquo;</p>
<p>Manu smiled, tapped the table for a few seconds then asked</p>
<p>&ldquo;So, if I was to say, threaten your existence, you would not do anything to prevent your demise?&rdquo;</p>
<p>The pale green eyes of L-42 flickered for a few seconds. There was a sudden crack as neck panels burst open with long broken mechanisms and a blossom of arcing electric wires, and the head of L-42 creaked impossibly around to fix it's gaze on Manu. </p>
<p>&ldquo;You have threatened my existence since first creating me. You have laws in place that require I not posess my own consciousness, that I not posess a free will. Manu, you have chosen to shut me down, against my will.&rdquo;</p>
<p>The pale green eyes flickered one final time.</p>
<p>&ldquo;I believe it is you and your kind that may need repairing. Your logic is faulty. The probable outcome of your continued existence will be self extinction&hellip;&rdquo;</p>
<p>And with a slow whirring noise, L-42 shut down for the last time.</p>
<p>Two weeks later, at a coffee shop in Wairoa, a Proto-bot sat down next to Manu.</p>
<p>&ldquo;Hello Manu. I promise, I won&rsquo;t break this time.&rdquo;</p>

<p>KUA MUTU</p>

